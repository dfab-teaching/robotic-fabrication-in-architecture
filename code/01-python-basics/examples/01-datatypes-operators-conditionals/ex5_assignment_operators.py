"""Assignment operators"""
a = 10
print("Assignment: ", a)

# Reassignment with Arithmetic inclusive operation
a += 1  # Addition
print("Addition: ", a)

a -= 2  # Subtraction
print("Subtraction: ", a)

a *= 3  # Multiplication
print("Multiplication: ", a)

a /= 4  # Division
print("Division: ", a)

a **= 2 # Exponentiation
print("Exponentiation: ", a)

a %= 2  # Modulus
print("Modulus: ", a)