"""Datatypes selection and assignment"""
a = "Datatypes"     # String
b = 10              # Integer
c = 5.5             # Float
d = True            # Boolean

print(a)
# print the data type
print(type(a))

print(b, type(b))
print(c, type(c))
print(d, type(d))

# typecast
my_float = float(b)
print("Variable b typecasted as: ", type(my_float))